function lYearDays(y) {
  var i,
    sum = 348;
  for (i = 0x8000; i > 0x8; i >>= 1) sum += asairlA_nl[y - yearA] & i ? 1 : 0;
  return sum + leapDays(y);
}

function leapDays(y) {
  if (leapMonth(y)) return asairlA_nl[y - yearA] & 0x10000 ? 30 : 29;
  else return 0;
}

function leapMonth(y) {
  return asairlA_nl[y - yearA] & 0xf;
}

function monthDays(y, m) {
  return asairlA_nl[y - yearA] & (0x10000 >> m) ? 30 : 29;
}

function Lunar(objDate) {
  var m = ""; // msg for log
  var i,
    leap = 0,
    temp = 0;
  var baseDate = new Date(yearA, 0, 31);
  // var offset = (objDate - baseDate) / 86400000;
  var offset = Math.floor((objDate.getTime() + 2206425600000) / 86400000);
  m +=
    "objDate=" +
    objDate.getTime() +
    ", new Date(" +
    yearA +
    ",0,31)=" +
    baseDate.getTime();
  m += "offset=" + offset;

  this.dayCyl = offset + 40;
  this.monCyl = 14;

  for (i = yearA; i < yearB && offset > 0; i++) {
    temp = lYearDays(i);
    offset -= temp;
    this.monCyl += 12;
  }

  if (offset < 0) {
    offset += temp;
    i--;
    this.monCyl -= 12;
  }

  this.year = i;
  this.yearCyl = i - 1864;

  leap = leapMonth(i);
  this.isLeap = false;

  for (i = 1; i < 13 && offset > 0; i++) {
    if (leap > 0 && i == leap + 1 && this.isLeap == false) {
      --i;
      this.isLeap = true;
      temp = leapDays(this.year);
    } else {
      temp = monthDays(this.year, i);
    }

    if (this.isLeap == true && i == leap + 1) this.isLeap = false;

    offset -= temp;
    if (this.isLeap == false) this.monCyl++;
  }

  if (offset == 0 && leap > 0 && i == leap + 1)
    if (this.isLeap) {
      this.isLeap = false;
    } else {
      this.isLeap = true;
      --i;
      --this.monCyl;
    }

  if (offset < 0) {
    offset += temp;
    --i;
    --this.monCyl;
  }

  this.month = i;
  this.day = offset + 1;

  m +=
    "\noffset=" +
    offset +
    ", year=" +
    this.year +
    ", yearCyl=" +
    this.yearCyl +
    ", month=" +
    this.month +
    ",\n monthCyl=" +
    this.monthCyl +
    ", day=" +
    this.day +
    ", dayCyl=" +
    this.dayCyl;
  log(2, m);
}

